// Operators in JS

// Arithmetic Operators
// +, -, *, /
let num1 = 10;
let num2 = 5;
console.log(num1);
console.log(num2);

let ans = num1 + num2;
console.log(ans);

ans = num1 - num2;
console.log(ans);

ans = num1 * num2;
console.log(ans);

ans = num1 / num2;
console.log(ans);


// Assignment Operators
// =, +=, -=, *=, %=, **=
let sum = 0;
console.log(sum);

sum += num1;
console.log(sum);

sum -= num2;
console.log(sum);

sum *= num1;
console.log(sum);

sum /= num2;
console.log(sum);

// Comparison Operators
// Equal to ---> ==
// Equal to & type ---> ===
// Not equal to ----> !=
// Not equal to & type ----> !==
// >, >=, <, <=

num1 = 20;
num2 = '20';

console.log(num1 == num2); // true both have same value
console.log(num1 === num2); // false since they have diff data type

// Logical Operators
// Logical AND ---> &&
// Logical OR ----> ||
// Logical Not ---> !

console.log(true && false);

console.log(true || false);

console.log(!true)